import java.util.*;
public class forloop {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n= sc.nextInt();
        int counter=1;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                counter++;
                System.out.format("%02d ",counter);
                               
            }
            System.out.println();
        }
    }
}





