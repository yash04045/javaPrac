

public class LeftShift {
    public static void main(String[] args) {
        System.out.println(5 << 1);  // Output: 10
        System.out.println(3 << 2);  // Output: 12
        System.out.println(1 << 3);  // Output: 8
        System.out.println(0 << 4);  // Output: 0
        System.out.println(-1 << 1); // Output: -2 (left shift of negative number)
    }
}
